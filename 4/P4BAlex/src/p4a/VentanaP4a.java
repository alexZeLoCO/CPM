    private void BEjecutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEjecutaActionPerformed
        // TODO add your handling code here:
        
        // Parámetros de la tarea
        int NI=SlIteraciones.getValue();
        TareaBack.setSumaMin(NI); 
        float SegsIter=Float.parseFloat(CBSegsIter.getSelectedItem().toString());
        TareaBack.setSegsInte(SegsIter);
        
        
        // Crear un hilo que lance la tarea
        HiloBack=new Thread() {
		public void run() {
			VentanaP4a.TareaBack.Run();
		}
	};        
        HiloBack.start(); // Lanzar el hilo (ejecutará Run)
        
        // Permitir que desde el nterfaz se pueda hacer Pause y Stop
        BStop.setEnabled(true);
        BEjecuta.setEnabled(false);        
    }//GEN-LAST:event_BEjecutaActionPerformed

